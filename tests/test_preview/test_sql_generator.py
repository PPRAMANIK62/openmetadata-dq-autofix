class TestGetTableParts:
    """Tests for SqlGenerator.get_table_parts()."""

    def test_full_fqn(self) -> None:
        """Test extracting parts from full FQN."""
        full_ref, simple_name = SqlGenerator.get_table_parts("db.schema.table")

        assert full_ref == "schema.table"
        assert simple_name == "table"

    def test_two_part_name(self) -> None:
        """Test extracting parts from two-part name."""
        full_ref, simple_name = SqlGenerator.get_table_parts("schema.table")

        assert full_ref == "schema.table"
        assert simple_name == "table"

    def test_simple_name(self) -> None:
        """Test extracting parts from simple name."""
        full_ref, simple_name = SqlGenerator.get_table_parts("table")

        assert full_ref == "table"
        assert simple_name == "table"
